    public void llenarTabla() {
    
        ControladorBD controlador = new ControladorBD();
        ArrayList<Venta> lista = controlador.obtenerVentas();
      
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(new String[]{"Id", "Fecha", "Descuento", "Cantidad", "Producto"});
         
        for (Venta v : lista) {
            modelo.addRow(new Object[]{ v.getId(), v.getFecha(), v.getDescuento(), v.getCantidad(), v.getProducto() });
        }
        
        tblVentas.setModel(modelo);
        //tblVentas.removeColumn(tblVentas.getColumnModel().getColumn(0));
    }

