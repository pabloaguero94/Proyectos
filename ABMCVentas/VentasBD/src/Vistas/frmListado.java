    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int fila = tblVentas.getSelectedRow();
        if(fila != -1)
        {
            int id = (int)tblVentas.getModel().getValueAt(fila, 0);

            ControladorBD controlador = new ControladorBD();
            boolean elimino = controlador.eliminarVenta(id);
            
            if(elimino)
                JOptionPane.showMessageDialog(this, "La venta se elimin√≥ exitosamente");
            else
                JOptionPane.showMessageDialog(this, "Hubo un problema al eliminar la venta");
        }
        else
            JOptionPane.showMessageDialog(this, "Seleccione la venta a eliminar");
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int fila = tblVentas.getSelectedRow();
        if(fila != -1)
        {
            TableModel modelo = tblVentas.getModel();
            int id = (int)modelo.getValueAt(fila, 0);
            String fecha = (String)modelo.getValueAt(fila, 1);
            int descuento = (int)modelo.getValueAt(fila, 2);
            int cantidad = (int)modelo.getValueAt(fila, 3);
            Producto producto = (Producto)modelo.getValueAt(fila, 4);
            Venta venta = new Venta(fecha, cantidad, descuento, producto);
            venta.setId(id);
            frmAlta ventana = new frmAlta(venta);
            ventana.setVisible(true);
        }
        else
            JOptionPane.showMessageDialog(this, "Seleccione la venta a actualizar");
    }//GEN-LAST:event_btnEditarActionPerformed

